"""Generate a local Pyroscope source-code map for the LGTM dev stack."""

from __future__ import annotations

import argparse
import collections.abc as cabc
import contextlib
import importlib.metadata
import importlib.util
import pathlib
import sys
from dataclasses import dataclass

ROOT = pathlib.Path(__file__).resolve().parents[2]
DEFAULT_OUTPUT = ROOT / ".tmp" / "lgtm" / ".pyroscope.yaml"


@dataclass(frozen=True, slots=True)
class LocalSource:
    """A repository-local source-code target."""

    path: str


@dataclass(frozen=True, slots=True)
class GitHubSource:
    """A GitHub source-code target."""

    owner: str
    repo: str
    ref: str
    path: str


@dataclass(frozen=True, slots=True)
class SourceMapping:
    """One Pyroscope source-code mapping."""

    prefixes: tuple[str, ...]
    language: str
    source: LocalSource | GitHubSource


@dataclass(frozen=True, slots=True)
class PythonPackageMappingSpec:
    """How an installed Python package maps back to upstream source."""

    module_name: str
    distribution_name: str
    owner: str
    repo: str
    source_path: str


