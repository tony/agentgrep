def build_mappings(
    *,
    repo_root: pathlib.Path,
    module_origins: cabc.Mapping[str, pathlib.Path | str] | None = None,
    distribution_versions: cabc.Mapping[str, str] | None = None,
) -> list[SourceMapping]:
    """Build Pyroscope mappings for this checkout and known dependencies."""
    del repo_root
    mappings = [
        SourceMapping(
            prefixes=("src",),
            language="python",
            source=LocalSource(path="src"),
        ),
        SourceMapping(
            prefixes=("scripts",),
            language="python",
            source=LocalSource(path="scripts"),
        ),
    ]

    origins = module_origins if module_origins is not None else _discover_module_origins()
    versions = (
        distribution_versions
        if distribution_versions is not None
        else _discover_distribution_versions(_PYTHON_PACKAGE_SPECS)
    )
    for spec in _PYTHON_PACKAGE_SPECS:
        origin = origins.get(spec.module_name)
        version = versions.get(spec.distribution_name)
        if origin is None or version is None:
            continue
        mappings.append(
            SourceMapping(
                prefixes=(spec.module_name.replace(".", "/"),),
                language="python",
                source=GitHubSource(
                    owner=spec.owner,
                    repo=spec.repo,
                    ref=f"v{version}",
                    path=spec.source_path,
                ),
            ),
        )
    return mappings


def render_pyroscope_yaml(mappings: cabc.Sequence[SourceMapping]) -> str:
    """Render ``mappings`` as a Pyroscope source-code YAML document."""
    lines = [
        "# Generated by scripts/lgtm/generate_pyroscope_source_map.py.",
        "# Generated output uses repository and package-relative prefixes.",
        "version: v1",
        "source_code:",
    ]
    if not mappings:
        lines.append("  mappings: []")
        return "\n".join(lines) + "\n"

    lines.append("  mappings:")
    for mapping in mappings:
        lines.append("    - path:")
        lines.extend(f"        - prefix: {prefix}" for prefix in mapping.prefixes)
        lines.append(f"      language: {mapping.language}")
        lines.append("      source:")
        if isinstance(mapping.source, LocalSource):
            lines.append("        local:")
            lines.append(f"          path: {mapping.source.path}")
        else:
            lines.append("        github:")
            lines.append(f"          owner: {mapping.source.owner}")
            lines.append(f"          repo: {mapping.source.repo}")
            lines.append(f"          ref: {mapping.source.ref}")
            lines.append(f"          path: {mapping.source.path}")
    return "\n".join(lines) + "\n"


def build_parser() -> argparse.ArgumentParser:
    """Build the command-line parser."""
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument(
        "--repo-root",
        type=pathlib.Path,
        default=ROOT,
        help="Repository checkout root used for local source prefixes.",
    )
    parser.add_argument(
        "--output",
        type=pathlib.Path,
        default=DEFAULT_OUTPUT,
        help="Output path. Use '-' to write the YAML to stdout.",
    )
    return parser


def main(argv: cabc.Sequence[str] | None = None) -> int:
    """Generate the source map."""
    args = build_parser().parse_args(argv)
    mappings = build_mappings(repo_root=args.repo_root)
    rendered = render_pyroscope_yaml(mappings)
    if str(args.output) == "-":
        sys.stdout.write(rendered)
        return 0
    args.output.parent.mkdir(parents=True, exist_ok=True)
    args.output.write_text(rendered, encoding="utf-8")
    return 0


def _discover_module_origins() -> dict[str, pathlib.Path]:
    """Return import origins for package specs that are importable."""
    origins: dict[str, pathlib.Path] = {}
    for spec in _PYTHON_PACKAGE_SPECS:
        with contextlib.suppress(Exception):
            module_spec = importlib.util.find_spec(spec.module_name)
            origin = getattr(module_spec, "origin", None)
            if origin and origin not in {"built-in", "frozen"}:
                origins[spec.module_name] = pathlib.Path(origin)
                continue
            search_locations = getattr(module_spec, "submodule_search_locations", None)
            if search_locations:
                origins[spec.module_name] = pathlib.Path(next(iter(search_locations)))
    return origins


def _discover_distribution_versions(
    specs: cabc.Iterable[PythonPackageMappingSpec],
) -> dict[str, str]:
    """Return installed versions for package specs that are installed."""
    versions: dict[str, str] = {}
    for spec in specs:
        with contextlib.suppress(importlib.metadata.PackageNotFoundError):
            versions[spec.distribution_name] = importlib.metadata.version(spec.distribution_name)
    return versions


if __name__ == "__main__":
    raise SystemExit(main())
